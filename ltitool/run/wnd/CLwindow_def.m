end

function btn_setdefault_doIt(source, callbackdata, CLwindow)


%
% Reload default values for closed loop system
%
%

% global variable
global _basePath;

% Complex variable "s"
s=tf('s'); 
% Plant, ME and FCE
Gp=(s+1)/(s^2+s+1); Gl=Gp; tiempo_muerto=0.0;
Gm=1/(0.1*s+1); Gt=Gm;
Gv=1/(0.2*s+1);
% controller
Kr=1.0; TI=1.0; TD=0.0;
Gc=Kr*(1+1/(TI*s));
% Compensator
Gcomp=(s+1)/(0.1*s+1);
% G(s)H(s)
GH=Gp*Gv*Gc*Gm;
% Simulation paramenters
amplitud_sp=1; amplitud_l=1;
T_sp=1; T_l=10; TFinal=20;

% Simulation window flags
haChCons=1;
haChCarga=1;

% Save data
save([_basePath filesep() 'data' filesep() 'loadCLdata'],'Gp','Gl','Gm','Gt','Gv','Gc','Gcomp','tiempo_muerto','GH','Kr','TI','TD')
save([_basePath filesep() 'data' filesep() 'loadCLsim'],'T_sp','T_l','TFinal','amplitud_sp','amplitud_l','haChCons','haChCarga')

end

