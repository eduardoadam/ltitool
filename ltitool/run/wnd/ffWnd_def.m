## @deftypefn  {} {} btn_FFaditionalinfo_doIt (@var{source}, @var{callbackdata}, @var{ffWnd})
##
## Define a callback for default action of btn_FFaditionalinfo control.
##
## @end deftypefn
function btn_FFaditionalinfo_doIt(source, callbackdata, ffWnd)


%
% Additional information for FFFB main window
% 

% global variable
global _ltitoolBasePath;

%  Load data
load([_ltitoolBasePath filesep() 'data' filesep() 'loadFFdata'])
load([_ltitoolBasePath filesep() 'data' filesep() 'loadFFsim'])

% Additional information for FFFB main window
% ------------------------------------------------------------------------
% Additional information about dynamic response
[estabilidad,yinf,Test]=additionalinfoFB(GH.num{1,1},GH.den{1,1},amplitud_sp,TFinal,1000);

% Additional information about frequency response
nGH=GH.num{1,1}; dGH=GH.den{1,1};
[ceros_la, polos_la,KGH]=xzpkdata(nGH,dGH);
nGH=nGH/KGH;
GH_K = tf(nGH,dGH); % 
Kasteric=KGH;
GH=tf(Kasteric*nGH,dGH);
[mGH,pGH,w]=bode(GH);
% fix the phase wrap arownd
phaud=pGH;
phw=unwrap(phaud*pi/180);
phaud=phw*180/pi;
% delayed plant phase,
%phad=phaud + ((-w.*delay)*180/pi)';
delay=0;
[Kast,Kast_ult,wultimo,GM,wcg,PM]=additionalinfofreq(GH,Kasteric,mGH,phaud,delay,w);


% ------------------------------------------------------------------------
% Put the additional information on FFFB main window
% ------------------------------------------------------------------------
%  Dynamic response
set(ffWnd.label_Estabilidad, "String", num2str(estabilidad));
set(ffWnd.valor_final, "String", num2str(yinf));
set(ffWnd.tiempo_estab_GH, "String", num2str(Test));
% Frequency response
set(ffWnd.Kasterisco, "String", num2str(Kast))
set(ffWnd.Kasterisco_ultimo, "String", num2str(Kast_ult))
set(ffWnd.wu, "String", num2str(wultimo))
set(ffWnd.gain_margin, "String", num2str(GM))
set(ffWnd.w_cg, "String", num2str(wcg))
set(ffWnd.phase_margin, "String", num2str(PM))

end

## @deftypefn  {} {} btn_setdefaultFF_doIt (@var{source}, @var{callbackdata}, @var{ffWnd})
##
## Define a callback for default action of btn_setdefaultFF control.
##
## @end deftypefn
function btn_setdefaultFF_doIt(source, callbackdata, ffWnd)


%
% Rewrite 2 files with predefined default data 
%
%

% global variable
global _ltitoolBasePath;

% Default data 
% ---------------------------------------------------------------------------------
% Complex variable "s"
s=tf('s'); 
% plant, ME y FCE
Gp=(s+1)/(s^2+s+1); Gl=Gp; tiempo_muerto=0.0;
Gm=1/(0.1*s+1); Gtl=Gm; Gt=Gm;
Gv=1/(0.1*s+1);
% controller
Kr=1.0; TI=1.0; TD=0.0;
Gc=Kr*(1+1/(TI*s));
% feedforward controller
Cff=1/(0.1*s+1);
% Compensator
Gcomp=(s+1)/(0.1*s+1);
% G(s)H(s)
GH=Gp*Gv*Gc*Gm;
% simulation parameters
amplitud_sp=1; amplitud_l=1;
T_sp=1; T_l=10; TFinal=20;

% simulation flags
haChCons=1;
haChCarga=1;

% Save data
save([_ltitoolBasePath filesep() 'data' filesep() 'loadFFdata'],'Gp','Gl','Gv','Gc','Gm','Gt', 'Gtl', 'GH', 'tiempo_muerto', ...
                                                         'Cff', 'Gcomp', 'Kr', 'TI', 'TD')
save([_ltitoolBasePath filesep() 'data' filesep() 'loadFFsim'],'T_sp','T_l','TFinal','amplitud_sp','amplitud_l','haChCons', 'haChCarga')      

end

