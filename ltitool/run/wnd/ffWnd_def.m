## @deftypefn  {} {} btn_saveFF_doIt (@var{source}, @var{callbackdata}, @var{ffWnd})
##
## Define a callback for default action of btn_saveFF control.
##
## @end deftypefn
function btn_saveFF_doIt(source, callbackdata, ffWnd)


%
% Save variables, transfer function, and so on
%

% global variable
global _ltitoolBasePath;

%  Load data
load([_ltitoolBasePath filesep() 'data' filesep() 'loadFFdata'])
load([_ltitoolBasePath filesep() 'data' filesep() 'loadFFsim'])


