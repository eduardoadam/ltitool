function btn_closeparamsimulGLC_doIt(source, callbackdata, CLsimparameters)


  % global variable
  global _basePath;

  % Obtengo datos de la ventana para despues grabarlos
  %
  amplitud_sp = eval(get(dlg.amplitud_sp, 'string'));
  T_sp = eval(get(dlg.T_sp, 'string'));
  amplitud_l = eval(get(dlg.amplitud_l, 'string'));
  T_l = eval(get(dlg.T_l, 'string'));
  TFinal = eval(get(dlg.TFinal, 'string'));
  haChCarga = get(dlg.cambio_carga, 'value');
  haChCons = get(dlg.cambio_SP, 'value');
  
  % Grabo datos de amplitud de escalones y tiempos de simulacion cargados en ventana
  save([_basePath filesep() 'data' filesep() 'loadCLsim'],'T_sp','T_l','TFinal','amplitud_sp','amplitud_l','haChCons','haChCarga')

  % Close window
  close(CLsimparameters.CLsimparameters);

end

 
